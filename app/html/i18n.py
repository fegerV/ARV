def normalize_locale(locale: str | None) -> str:
    if not locale:
        return DEFAULT_LANGUAGE
    normalized = locale.strip().lower()
    return normalized if normalized in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def translate(key: str, locale: str | None = None, **params: Any) -> str:
    active_locale = normalize_locale(locale)
    template = (
        TRANSLATIONS.get(active_locale, {}).get(key)
        or TRANSLATIONS[DEFAULT_LANGUAGE].get(key)
        or key
    )
    if params:
        try:
            return template.format(**params)
        except Exception:
            return template
    return template


def get_request_locale(request: Any) -> str:
    session = {}
    try:
        session = getattr(request, "session", {}) or {}
    except AssertionError:
        session = getattr(request, "scope", {}).get("session", {}) or {}

    session_locale = session.get(SESSION_LANGUAGE_KEY)
    if session_locale:
        locale = normalize_locale(session_locale)
    else:
        state_locale = getattr(getattr(request, "state", None), "locale", None)
        locale = normalize_locale(state_locale)

    try:
        request.state.locale = locale
    except Exception:
        pass

    return locale


@pass_context
def t(context: dict[str, Any], key: str, **params: Any) -> str:
    request = context.get("request")
    locale = get_request_locale(request) if request is not None else DEFAULT_LANGUAGE
    return translate(key, locale, **params)
